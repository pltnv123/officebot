# === Основной планировщик ===

class ContentPlanner:
    """Планировщик контента для VK"""
    
    def __init__(self):
        self.post_history = []
    
    def get_post_for_time(self, hour: int) -> dict:
        """Возвращает подходящий пост для времени дня"""
        if 8 <= hour < 11:
            posts = MORNING_POSTS
        elif 12 <= hour < 15:
            posts = NOON_POSTS
        elif 18 <= hour < 20:
            posts = EVENING_POSTS
        elif 20 <= hour < 23:
            posts = NIGHT_POSTS
        else:
            posts = NIGHT_POSTS  # fallback
        
        # Выбираем пост который ещё не использовали сегодня
        available = [p for p in posts if p not in self.post_history[-len(posts):]]
        if not available:
            available = posts
        
        post = random.choice(available)
        self.post_history.append(post)
        return post
    
    def get_daily_plan(self) -> list:
        """Возвращает план на день (4 поста)"""
        now = datetime.now()
        
        plan = []
        time_slots = [
            ("morning", 9),
            ("noon", 13),
            ("evening", 19),
            ("night", 21),
        ]
        
        for slot_name, hour in time_slots:
            post = self.get_post_for_time(hour)
            post["time"] = slot_name
            post["hour"] = hour
            plan.append(post)
        
        return plan
    
    def format_post(self, post: dict) -> str:
        """Форматирует пост в casual стиль"""
        if post["type"] == "text":
            return make_casual_post(post["content"])
        elif post["type"] == "poll":
            return post["content"]
        return post["content"]


