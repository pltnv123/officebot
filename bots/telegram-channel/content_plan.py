# ─── Темы по дням недели для разнообразия ───
DAILY_THEMES = {
    0: "ai_work",          # Понедельник
    1: "automation",       # Вторник
    2: "programming",      # Среда
    3: "business_ai",      # Четверг
    4: "cases",            # Пятница
    5: "productivity",     # Суббота
    6: "ai_work",          # Воскресенье
}

def get_daily_theme(day_of_week: int) -> str:
    """Получить тему для дня недели"""
    return DAILY_THEMES.get(day_of_week, "ai_work")

def get_random_topic(category: str) -> str:
    """Получить случайную тему из категории"""
    topics = TOPICS.get(category, TOPICS["ai_work"])
    return random.choice(topics)

def get_post_prompt(post_type: str, **kwargs) -> str:
    """Получить промт для генерации поста"""
    prompt = POST_PROMPTS.get(post_type, POST_PROMPTS["greeting"])
    
    # Подставляем переменные если есть
    for key, value in kwargs.items():
        prompt = prompt.replace(f"{{{key}}}", str(value))
    
    return prompt

def get_all_topics() -> list:
    """Получить список всех тем"""
    all_topics = []
    for category in TOPICS.values():
        all_topics.extend(category)
    return all_topics
